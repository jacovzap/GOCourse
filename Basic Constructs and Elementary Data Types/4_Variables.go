package main
import "fmt"

func main(){
    var number int          // Declaring  an integer variable 
    fmt.Println(number)     // Printing its value
    var decision bool       // Declaring a boolean variable
    fmt.Println(decision)   // Printing its value

	var number_ int = 5          // Declaring and initializing  an integer variable 
    fmt.Println(number_)          // Printing its value
    var decision_ bool = true     // Declaring a initializing a boolean variable
    fmt.Println(decision_) 

	//PODEMOS REEMPLAZAR EL KEYWORD VAR
	number_example := 7
	decition := true
	fmt.Println(number_example)
	fmt.Println(decition)

}